package com.reos79.rmm.model;

/**
 * The auth token
 * @author reos79
 *
 */
public class Token {

	/**
	 * The token value
	 */
	private String token;

	/**
	 * Returns the token value
	 * @return The token value
	 */
	public String getToken() {
		return token;
	}

	/**
	 * Sets the token value
	 * @param token The token value
	 */
	public void setToken(String token) {
		this.token = token;
	}
}
